public class SquaresOfNumbersFrom1To10 {
    public static void main(String[] args) {
        int num = 10;

        System.out.println("Square of 1 to 10 is following ");

        int i = 0;
        while (i<num) {
            //System.out.println(i);
            i++;
            System.out.println("Square of "+i+" is "+(i*i));
        }
    }
    
}
